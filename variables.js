 // Variables memory locations hoti hain jo program execution ke dauran data ko store aur manage karne ke liye use hoti hain. JavaScript me variables ko var, let aur const keyword se declare kiya jata hai.



 /// // Variables data store karne ke liye use hoti hain.

// var - Old way (Function Scoped)
var name = "Rishabh";
console.log(name);

// let - Value change ho sakti hai (Block Scoped)
let age = 22;
age = 23;
console.log(age);

// const - Constant value, change nahi hoti (Block Scoped)
const country = "India";
console.log(country);///